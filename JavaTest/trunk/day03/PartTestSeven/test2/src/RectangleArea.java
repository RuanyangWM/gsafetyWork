public class RectangleArea {

        private float height;
        private float width;
        public RectangleArea(float height,float width){
            this.height=height;
            this.width=width;
        }
        public float square(){
            return height*width;
        }
        public static void main(String args[]){
            RectangleArea u=new RectangleArea(3.5f,4.5f);
            System.out.println(u.square());
        }
   
}
