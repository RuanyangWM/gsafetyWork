public class TransferNum {
    /*
    将所有整型转换成int型
     */
    public static void main(String[] args) {
        long longNum=20018;
        short shortNum=2018;
        byte byteNum=18;
        int longInt=(int) longNum;
        int shortInt=(int) shortNum;
        int byteInt=(int)byteNum;
        System.out.println("longInt:"+longInt);
        System.out.println("shortInt:"+shortInt);
        System.out.println("byteInt:"+byteInt);
    }
}
